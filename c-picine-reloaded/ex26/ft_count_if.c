int	ft_count_if(char **tab, int (*f)(char*))
{
	int	i;
	int	a;

	i = 0;
	a = 0;
	while (tab[a])
	{
		if (f(tab[a]) == 1)
			i++;
		a++;
	}
	return (i);
}
